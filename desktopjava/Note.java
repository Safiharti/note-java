    private void liste_note() {
        DefaultTableModel model1=new DefaultTableModel();
        model1.addColumn("CODE MATIERE");
        model1.addColumn("N° Inscription");
        model1.addColumn("NOTE");
        model1.addColumn("NIVEAU");
        jTable1.setModel(model1);
        try
        {
            Statement st=conn.start().createStatement();
            ResultSet res=st.executeQuery("SELECT * FROM notes");
            while(res.next())
            {
                model1.addRow(new Object[]{
                    res.getString(1),
                    res.getString(2),
                    res.getFloat(3),
                    res.getString(4)
                });
            }
        }catch(SQLException e)
        {
            System.err.println(e.getMessage());
        }
    }
    
    private void code_matiere(){
        DefaultComboBoxModel<String> model1=new DefaultComboBoxModel();
        try
        {
            Statement st=conn.start().createStatement();
            ResultSet res=st.executeQuery("SELECT codemat FROM matiere");
            while(res.next())
            {
                String code=res.getString(1);
                model1.addElement(code);
            }
        }catch(SQLException e)
        {
            System.err.println(e.getMessage());
        }
        codemat.setModel(model1);
    }
    
