    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        String code,num,anee;
        Float notes;
        code=codemat.getSelectedItem().toString();
        num=numero.getSelectedItem().toString();
        notes=Float.valueOf(note.getText());
        anee=annee.getSelectedItem().toString();
        try{
            PreparedStatement pre=conn.start().prepareStatement("INSERT INTO notes VALUES (?,?,?,?)");
            pre.setString(1,code);
            pre.setString(2,num);
            pre.setFloat(3,notes);
            pre.setString(4, anee);
            pre.executeUpdate();
            JOptionPane.showConfirmDialog(jLayeredPane1,"Note enregistré", "Information", JOptionPane.CLOSED_OPTION,JOptionPane.INFORMATION_MESSAGE);
        }catch(SQLException e)
        {
            JOptionPane.showConfirmDialog(jLayeredPane1,e.getMessage(), "Erreur", JOptionPane.OK_OPTION,JOptionPane.ERROR_MESSAGE);
        }finally{initial();}
    }//GEN-LAST:event_addActionPerformed

