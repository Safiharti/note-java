    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        String libele,code;
        Float coeff;
        code=codemat.getText();
        libele=libelle.getText();
        coeff=Float.valueOf(coef.getText());
        try{
            PreparedStatement pre=conn.start().prepareStatement("INSERT INTO matiere VALUES (?,?,?)");
            pre.setString(1,code);
            pre.setString(2,libele);
            pre.setFloat(3,coeff);
            pre.executeUpdate();
            JOptionPane.showConfirmDialog(jLayeredPane1,"Matière enregistré", "Information", JOptionPane.CLOSED_OPTION,JOptionPane.INFORMATION_MESSAGE);
        }catch(SQLException e)
        {
            JOptionPane.showConfirmDialog(jLayeredPane1,e.getMessage(), "Erreur", JOptionPane.OK_OPTION,JOptionPane.ERROR_MESSAGE);
        }finally{initial();}
    }//GEN-LAST:event_addActionPerformed

