    private void liste_matiere() {
        DefaultTableModel model1=new DefaultTableModel();
        model1.addColumn("CODE MATIERE");
        model1.addColumn("LIBELLE");
        model1.addColumn("COEFFICIENT");
        jTable1.setModel(model1);
        try
        {
            Statement st=conn.start().createStatement();
            ResultSet res=st.executeQuery("SELECT * FROM matiere");
            while(res.next())
            {
                model1.addRow(new Object[]{
                    res.getString(1),
                    res.getString(2),
                    res.getFloat(3)
                });
            }
        }catch(SQLException e)
        {
            System.err.println(e.getMessage());
        }
    }
