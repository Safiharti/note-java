    private void numInscription(){
        DefaultComboBoxModel<String> model_et=new DefaultComboBoxModel();
        try
        {
            Statement st=conn.start().createStatement();
            ResultSet res=st.executeQuery("SELECT n°Inscription FROM etudiant");
            while(res.next())
            {
                model_et.addElement(res.getString(1));
            }
        }catch(SQLException e)
        {
            System.err.println(e.getMessage());
        }
        numero.setModel(model_et);
    }
