    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String numInscr=numero.getSelectedItem().toString();
       DefaultTableModel model1=new DefaultTableModel();
        model1.addColumn("DESIGNATION");
        model1.addColumn("COEFFICIENT");
        model1.addColumn("NOTE");
        model1.addColumn("NOTE PONDEREE");
        jTable1.setModel(model1);
       try
        {
            Statement st=conn.start().createStatement();
            ResultSet res=st.executeQuery("SELECT etudiant.n°Inscription,etudiant.Nom,matiere.Libelle,matiere.Coef,notes.note,"
                    + "(notes.note*matiere.Coef) FROM etudiant JOIN notes ON etudiant.n°Inscription=notes.n°Inscription "
                    + "JOIN matiere ON matiere.codemat=notes.codemat WHERE etudiant.n°Inscription='"+numInscr+"'");
            while(res.next())
            {
                num.setText(res.getString(1));
                name.setText(res.getString(2));
                model1.addRow(new Object[]{
                    res.getString(3),
                    res.getFloat(4),
                    res.getFloat(5),
                    res.getFloat(6)
                }); 
            }
            ResultSet res1=st.executeQuery("SELECT SUM((notes.note*matiere.Coef))/(SUM(matiere.Coef)),CASE WHEN SUM((notes.note*matiere.Coef))/(SUM(matiere.Coef)) < 7.5 THEN 'Exclu' WHEN SUM((notes.note*matiere.Coef))/(SUM(matiere.Coef)) >= 7.5 AND SUM((notes.note*matiere.Coef))/(SUM(matiere.Coef)) < 10 THEN 'Redoublant' ELSE 'Admis' END AS statut FROM etudiant JOIN notes ON etudiant.n°Inscription=notes.n°Inscription JOIN matiere ON matiere.codemat=notes.codemat WHERE etudiant.n°Inscription='"+numInscr+"' GROUP BY etudiant.n°Inscription");
            while(res1.next())
            {
                moyenne.setText(String.valueOf(res1.getFloat(1)));
                obs.setText(res1.getString(2));
            }
        }catch(SQLException e)
        {
            System.err.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        PrinterJob print_bulletin=PrinterJob.getPrinterJob();
        print_bulletin.setJobName("Bulletin");
        print_bulletin.setPrintable(new Printable(){
            @Override
            public int print(Graphics gr,PageFormat pf,int page){
                pf.setOrientation(PageFormat.LANDSCAPE);
                if (page>0) {
                    return Printable.NO_SUCH_PAGE;
                }
                Graphics2D g2=(Graphics2D) gr;
                g2.translate(pf.getImageableX(), pf.getImageableY());
                g2.scale(1, 1);
                jPanel1.print(g2);
                return Printable.PAGE_EXISTS;               
            }
        });
        boolean ok = print_bulletin.printDialog();
        if(ok){
        try{
            
        print_bulletin.print();
        }
        catch (PrinterException ex){
	ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

